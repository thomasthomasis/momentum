// Energy level indicator — 5 small bars like a signal/battery meter
export function EnergyBars({ level }: { level: number }) {
  return (
    <span className="energy-bars" aria-label={`Energy ${level}/5`}>
      {[1, 2, 3, 4, 5].map(i => (
        <span
          key={i}
          className="energy-bar"
          style={{ opacity: i <= level ? 1 : 0.2 }}
        />
      ))}
    </span>
  )
}